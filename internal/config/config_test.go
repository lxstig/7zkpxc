// resetViper clears all global viper state so tests are fully isolated.
func resetViper(t *testing.T) {
	t.Helper()
	viper.Reset()
}

func TestLoadConfig_Defaults(t *testing.T) {
	resetViper(t)

	tmpHome := t.TempDir()
	t.Setenv("HOME", tmpHome)

	// Write a minimal config with only kdbx_path so we can verify that all
	// other fields receive their correct default values via LoadConfig.
	configDir := filepath.Join(tmpHome, ".config", "7zkpxc")
	if err := os.MkdirAll(configDir, 0o755); err != nil {
		t.Fatalf("failed to create config dir: %v", err)
	}
	minimalYAML := "general:\n  kdbx_path: /tmp/test.kdbx\n"
	if err := os.WriteFile(filepath.Join(configDir, "config.yaml"), []byte(minimalYAML), 0o644); err != nil {
		t.Fatalf("failed to write minimal config: %v", err)
	}

	loaded, err := LoadConfig()
	if err != nil {
		t.Fatalf("LoadConfig() failed: %v", err)
	}

	if loaded.General.DefaultGroup != "Archives/AutoGenerated" {
		t.Errorf("DefaultGroup = %q, want %q", loaded.General.DefaultGroup, "Archives/AutoGenerated")
	}
	if loaded.General.UseKeyring != true {
		t.Errorf("UseKeyring = %v, want true", loaded.General.UseKeyring)
	}
	if loaded.General.PasswordLength != PasswordLengthDefault {
		t.Errorf("PasswordLength = %d, want %d", loaded.General.PasswordLength, PasswordLengthDefault)
	}
	if loaded.SevenZip.BinaryPath != "7z" {
		t.Errorf("BinaryPath = %q, want %q", loaded.SevenZip.BinaryPath, "7z")
	}
	if len(loaded.SevenZip.DefaultArgs) != 2 {
		t.Errorf("DefaultArgs len = %d, want 2", len(loaded.SevenZip.DefaultArgs))
	}
}

func TestLoadConfig_NoConfigFile_RequiresInit(t *testing.T) {
	resetViper(t)

	tmpHome := t.TempDir()
	t.Setenv("HOME", tmpHome)

	// No config file exists — LoadConfig should now fail with an actionable message
	// because kdbx_path is required and must be set via `7zkpxc init`.
	_, err := LoadConfig()
	if err == nil {
		t.Fatal("LoadConfig() should fail when kdbx_path is not set (init never run)")
	}
	if !strings.Contains(err.Error(), "7zkpxc init") {
		t.Errorf("error message should mention '7zkpxc init', got: %v", err)
	}
}

